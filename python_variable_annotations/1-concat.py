#!/usr/bin/env python3
"""
This module provides a function to concatenate two strings
"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenate two strings

    Args:
        str1 (str) : First String
        str2 (str) : Second String

    Returns:
        str: The concatenated string
    """
    return str1 + str2

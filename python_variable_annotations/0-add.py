#!/usr/binenv pyhton3
"""
This module provides a function to add two floating-point numbers.
"""


def add(a: float, b: float) -> float:

    """
    Adds two float numbers:
    Args:
        a (float): Fisrt number
        b (float): Second number
    Returns:
        float: the addition of a and b
    """

    return a + b
